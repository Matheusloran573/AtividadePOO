public class Usuario {
    String nome;
    String email;
    double investimento;
    double orcamento;
    double meta;
    String conta;

    public Usuario(String nome, String email, double investimento, double orcamento, double meta, String conta){
        this.nome = nome;
        this.email = email;
        this.investimento = investimento;
        this.orcamento = orcamento;
        this.meta = meta;
        this.conta = conta;
    }
}
