public class Usuario {
    String nome;
    String email;
    double investimentos;
    double orcamento;
    double metas;
    String conta;

    public Usuario(String nome, String email, double investimentos, double orcamento, double metas, String conta){
        this.nome = nome;
        this.email = email;
        this.investimentos = investimentos;
        this.orcamento = orcamento;
        this.metas = metas;
        this.conta = conta;
    }
}
